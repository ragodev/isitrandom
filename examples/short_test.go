package examples

import (
	"bytes"
	"testing"

	"github.com/tscholl2/isitrandom"
)

// 1110001100010001010011101111001001001001 x 4
var short = bytes.NewBuffer([]byte{
	0xe3, 0x11, 0x4e, 0xf2, 0x49, 0xe3, 0x11, 0x4e,
	0xf2, 0x49, 0xe3, 0x11, 0x4e, 0xf2, 0x49, 0xe3,
	0x11, 0x4e, 0xf2, 0x49})

func TestShortRNG(t *testing.T) {
	isitrandom.TestRNG(t, short)
}
